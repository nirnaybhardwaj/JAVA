public class Important_values {
    public static void main(String[] args) {

        //GREATER THAN 2 AND 3 NUMBERS

        int a = 903;
        int b = 70;
        int c = 56;

        int ans = Math.max(a,b);
        System.out.println(ans);

        int ans34 = Math.min(a,b);
        System.out.println(ans34);

        int anss = Math.max(a,Math.max(b,c));
        System.out.println(anss);

        int anssa = Math.min(a,Math.min(b,c));
        System.out.println(anssa);

            //POWER

        int ans12 = (int)Math.pow(2,3);
        System.out.println(ans12);

//        SQUAREROOT

        int ans1 = (int)Math.sqrt(36);
        System.out.println(ans1);

        //MAXIMUM VALUE OF INTEGER, DOUBLE, BYTE

        int ans2 = Integer.MAX_VALUE;
        System.out.println(ans2);

        //MINIMUM VALUE OF INTEGER, DOUBLE, BYTE

        int ans3 = Integer.MIN_VALUE;
        System.out.println(ans3);
    }
}
